/// <summary>
/// Defines the categories supported by the wardrobe system.
/// Aligns with <see cref="WardrobeTabType"/> for UI filtering and slot binding.
/// </summary>
public enum WardrobeCategory
{
    /// <summary>
    /// Hair styles and hair accessories.
    /// </summary>
    Hair,

    /// <summary>
    /// General accessories such as necklaces or earrings.
    /// </summary>
    Accessories,

    /// <summary>
    /// Glasses or other eyewear items.
    /// </summary>
    Eyewear,

    /// <summary>
    /// Tops such as shirts or jackets.
    /// </summary>
    Tops,

    /// <summary>
    /// Bottoms such as pants or skirts.
    /// </summary>
    Pants,

    /// <summary>
    /// One-piece outfits that cover both top and bottom.
    /// </summary>
    OnePiece,

    /// <summary>
    /// Footwear items.
    /// </summary>
    Shoes
}
